package org.liberator.ratdriver.enums;

/**
 * A list of driver types
 */
public enum DriverType {
    /**
     * Driver not specified
     */
    NotSpecified,

    /**
     * Firefox web driver
     */
    FirefoxDriver,

    /**
     * Chrome web driver
     */
    ChromeDriver,

    /**
     * Edge web driver
     */
    EdgeDriver,

    /**
     * Internet Explorer web driver
     */
    InternetExplorerDriver,

    /**
     * Opera web driver
     */
    OperaDriver,

    /**
     * Safari Web Driver
     */
    SafariDriver,

    /**
     * Remote Web Driver
     */
    RemoteDriver
}
